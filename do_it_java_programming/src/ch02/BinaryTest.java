package ch02;

public class BinaryTest {
    public static void main(String[] args) {
        //10 진수
        int num = 10;
        // 2진수
        int bNum = 0b1010;
        // 8진수
        int oNum = 012;
        // 16진수
        int xNum = 0xA;

        System.out.println(num);
        System.out.println(bNum);
        System.out.println(oNum);
        System.out.println(xNum);

    }
}